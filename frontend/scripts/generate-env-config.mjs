/**
 * Writes public/env-config.js before vite build so static hosting (e.g. Vercel)
 * gets a public API base URL from CI env vars — not a committed LAN address.
 */
import { writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = join(__dirname, '..', 'public', 'env-config.js');

const raw =
  process.env.VITE_API_URL ||
  process.env.VITE_API_BASE_URL ||
  '';
const url = String(raw).trim().replace(/\/+$/, '');

const content = `/* Generated by scripts/generate-env-config.mjs at build time. Do not edit. */
window._env_ = {
  REACT_APP_API_URL: ${JSON.stringify(url)}
};
`;

writeFileSync(outPath, content, 'utf8');
// eslint-disable-next-line no-console
console.log(
  '[generate-env-config] REACT_APP_API_URL:',
  url || '(empty - local dev uses Vite /api proxy)'
);

if (process.env.VERCEL === '1' && !url) {
  // eslint-disable-next-line no-console
  console.warn(
    '[generate-env-config] Vercel build: set VITE_API_URL to your HTTPS backend (e.g. https://api.example.com) or the site cannot load data.'
  );
}
